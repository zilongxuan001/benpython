#要做的事情
# 1.使用你的浏览器登录 bitbucket.org，搜索 “python”。
# 2.忽略那些提到 “Python 3” 的项目，它们只会让你变迷糊。
# 3.随便找一个项目，然后点进去。
# 3.点击 Source 标签，浏览目录和文件列表，直到你看到以 .py 结尾的文件（setup.py 就别看了，这样的文件看了也没用）。
# 4.从头开始阅读你找到的代码。把它的功能用笔记记下来。
# 5.如果你看到一些有趣的符号或者奇怪的字串，你可以把它们记下来，日后再进行研究。

